#include "ft_colle.h"

int		ft_atoi(char *str)
{
	int i;
	int ret;

	i = 0;
	ret = 0;
	while (str[i] != '\0')
	{
		ret = ret * 10 + str[i] - 48;
		i++;
	}
	return (ret);
}
